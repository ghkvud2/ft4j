package com.github.ghkvud2.ft4j.exception;

public class GeneratedValueExceedsLimitException extends RuntimeException {

	private static final long serialVersionUID = -8476646957418697874L;

	public GeneratedValueExceedsLimitException() {
		super();
	}

	public GeneratedValueExceedsLimitException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public GeneratedValueExceedsLimitException(String message, Throwable cause) {
		super(message, cause);
	}

	public GeneratedValueExceedsLimitException(String message) {
		super(message);
	}

	public GeneratedValueExceedsLimitException(Throwable cause) {
		super(cause);
	}

}